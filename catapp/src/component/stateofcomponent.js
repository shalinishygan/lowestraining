/**
 * State is like a component’s personal data storage.
 * State is useful for handling data that changes over time 
 * or that comes from user interaction. 
 * State gives your components memory!
 * As a general rule, use props to configure a component when it renders. 
 * Use state to keep track of any component data that you expect to change over time.
 * 
 * 
 * Example
 * The following example takes place in a cat cafe where 
 * two hungry cats are waiting to be fed. 
 * Their hunger, which we expect to change over time (unlike their names), 
 * is stored as state. 
 * To feed the cats, press their buttons—which will update their state.
 * 
 * Example using Functional Component
 * We can add state to a component by calling React’s useState Hook. 
 * A Hook is a kind of function that lets you “hook into” React features. 
 * For example, useState is a Hook that lets you add state to function components.
 * 
 */
